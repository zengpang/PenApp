package com.example.administrator.penapp.DIY_List;

public class Sounds {
    String text;
    String title;
    String time;
    String path;
    String sounds_name;

    public String getSounds_name() {
        return sounds_name;
    }

    public void setSounds_name(String sounds_name) {
        this.sounds_name = sounds_name;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
